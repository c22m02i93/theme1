      function addPlacemarksToMap(locs) {
        clusterer.removeAll();
        var newPlacemarks = locs.map(location => {
          var balloonContent = {
            name: location.name,
            image: location.image,
            address: location.address,
            deanery: location.deanery,
            link: location.permalink,
            custom_field: location.custom_field
          };

          var icon = location.balloon_type === 'monastery' ?
            'https://kostromamitropolia.ru/local/templates/ke/images/monastery.svg' :
            'https://kostromamitropolia.ru/local/templates/ke/images/hram.svg';

          return new ymaps.Placemark([location.latitude, location.longitude], balloonContent, {
            balloonContentLayout: MyBalloonLayout,
            iconLayout: 'default#image',
            iconImageHref: icon,
            iconImageSize: [45, 57],
            iconImageOffset: [-22.5, -57]
          });
        });

        clusterer.add(newPlacemarks);
        myMap.geoObjects.add(clusterer);
        placemarks = newPlacemarks;
      }

