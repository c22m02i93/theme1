      function getEasterDateForYear(year) {
        if (orthodoxEasterDates[year]) {
          return new Date(orthodoxEasterDates[year]);
        }
        return null;
      }

      function getHolidayDate(holiday) {
        const yearForHoliday = currentYear; 
        const easterDate = getEasterDateForYear(yearForHoliday);
        if (!easterDate) return null;

        let offset = parseInt(holiday.offset_from_easter, 10);
        if (!isNaN(offset)) {
          if (offset > 0) {
            offset -= 1;
          } else if (offset < 0) {
            offset += 1;
          }
          const date = new Date(easterDate);
          date.setDate(date.getDate() + offset);
          return date;
        } else if (holiday.day && holiday.month) {
          const date = new Date(yearForHoliday, holiday.month - 1, holiday.day);
          return date;
        }
        return null;
      }

      function getYearsSince(year) {
        if (!year) return '';
        const diff = currentYear - year;
        if (diff > 0) {
          return ` (${diff} лет)`;
        } else if (diff === 0) {
          return ' (в этом году)';
        }
        return '';
      }

      function loadHolidaysForMonth(monthIndex) {
        let holidaysForMonth = [];
        allHolidaysData.forEach(loc => {
          loc.holidays.forEach(h => {
            const holidayDate = getHolidayDate(h);
            if (holidayDate && holidayDate.getMonth() === monthIndex && holidayDate.getFullYear() === currentYear) {
              holidaysForMonth.push({
                location_id: loc.location_id,
                location_name: loc.location_name,
                holiday_name: h.holiday_name,
                holiday_year: h.year,
                date: holidayDate
              });
            }
          });
        });

        holidaysForMonth.sort((a, b) => a.date - b.date);

        let html = `<h4>Текущий месяц: ${monthNames[monthIndex]} ${currentYear}</h4>`;
        if (holidaysForMonth.length === 0) {
          html += '<p>В этом месяце нет праздников.</p>';
        } else {
          html += `
            <table class="table table-striped mb-0">
              <thead>
                <tr>
                  <th>Дата</th>
                  <th>Храм</th>
                  <th>Праздник</th>
                </tr>
              </thead>
              <tbody>
          `;

          holidaysForMonth.forEach(h => {
            const day = String(h.date.getDate()).padStart(2,'0');
            const month = String(h.date.getMonth()+1).padStart(2,'0');
            const dateStr = `${day}.${month}`;
            const yearsStr = getYearsSince(h.holiday_year);
            const loc = locationMap[h.location_id];
            const templeLink = loc ? loc.permalink : '#';

            html += `
              <tr>
                <td>${dateStr}</td>
                <td><a href="${templeLink}" target="_blank">${h.location_name}</a></td>
                <td>${h.holiday_name}${yearsStr}</td>
              </tr>
            `;
          });

          html += `</tbody></table>`;
        }

        calendarContent.innerHTML = html;
      }
